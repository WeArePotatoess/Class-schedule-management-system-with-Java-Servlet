package Model;

/**
 *
 * @author Admin
 */
public class Courses {
    private String id,name,code,departmentID;

    public Courses() {
    }

    public Courses(String id, String name, String code, String departmentID) {
        this.id = id;
        this.name = name;
        this.code = code;
        this.departmentID = departmentID;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getDepartmentID() {
        return departmentID;
    }

    public void setDepartmentID(String departmentID) {
        this.departmentID = departmentID;
    }
    
}
